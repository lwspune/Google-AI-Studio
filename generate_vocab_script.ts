import { GoogleGenAI, Type } from "@google/genai";

const ai = new GoogleGenAI({ apiKey: process.env.GEMINI_API_KEY });

async function generateVocab(words: string[], startId: number) {
  const response = await ai.models.generateContent({
    model: "gemini-3.1-pro-preview",
    contents: `Generate a JSON array of VocabWord objects for the following words: ${words.join(', ')}.
    The VocabWord interface is:
    interface VocabWord {
      id: string;
      word: string;
      partOfSpeech: string;
      definition: string;
      example: string;
      synonyms: string[];
      antonyms: string[];
      difficulty: 'common' | 'basic' | 'advanced';
    }
    Start IDs from ${startId}. Return ONLY the JSON array.`,
    config: {
      responseMimeType: "application/json",
      responseSchema: {
        type: Type.ARRAY,
        items: {
          type: Type.OBJECT,
          properties: {
            id: { type: Type.STRING },
            word: { type: Type.STRING },
            partOfSpeech: { type: Type.STRING },
            definition: { type: Type.STRING },
            example: { type: Type.STRING },
            synonyms: { type: Type.ARRAY, items: { type: Type.STRING } },
            antonyms: { type: Type.ARRAY, items: { type: Type.STRING } },
            difficulty: { type: Type.STRING, enum: ['common', 'basic', 'advanced'] },
          },
          required: ["id", "word", "partOfSpeech", "definition", "example", "synonyms", "antonyms", "difficulty"],
        },
      },
    },
  });
  return response.text;
}

